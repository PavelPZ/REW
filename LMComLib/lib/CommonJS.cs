  public struct Currency {

#if !SILVERLIGHT
    static int VAT = int.Parse(ConfigurationManager.AppSettings["Fact.TaxPercent"] ?? "20");
#else
    static int VAT = 20;
#endif
    public Currency(CurrencyType typ, double amount) {
      Typ = typ; Amount = amount; WithVat = false; Ptr = null; SubSites = null;
    }
    public Currency(CurrencyType typ, double amount, bool withVat) {
      Typ = typ; Amount = amount; WithVat = false; WithVat = withVat; Ptr = null; SubSites = null;
    }

    public CurrencyType Typ;
    public double Amount;
    public bool WithVat;
    public string Ptr; //identifikace ceny, cena zadana identifikatorem do tabulky cen
    public SubSiteCurrency[] SubSites; //cena je jazykove zavisla

    public delegate Currency PtrAmountEvent(string ptr, SubDomains subsite, ProductLicenceType lic);
    public static PtrAmountEvent OnGetAmount;

    Currency getValue(SubDomains subsite, ProductLicenceType lic) { if (subsite != SubDomains.no && Ptr != null && OnGetAmount != null) return OnGetAmount(Ptr, subsite, lic); else return this; }

    //public static double priceTax(double Amount, bool WithVat) { return priceTax(Amount, WithVat, VAT); }
    //public static double price(double Amount, bool WithVat) { return price(Amount, WithVat, VAT); }
    public static double priceTax(double Amount, bool WithVat, double vat) { return RoundCurrency(WithVat ? Amount : (Amount * (vat / 100 + 1))); }
    public static double price(double Amount, bool WithVat, double vat) { return RoundCurrency(WithVat ? (Amount / (vat / 100 + 1)) : Amount); }

    public Currency priceValue(SubDomains subSite, ProductLicenceType lic) {
      if (SubSites == null) return getValue(subSite, lic);
      foreach (SubSiteCurrency lc in SubSites) if (lc.SubSite == subSite) return lc.getValue(OnGetAmount, lic);
      return getValue(subSite, lic);
    }

    public double PriceTax(SubDomains subSite, ProductLicenceType lic) { return PriceTax(VAT, subSite, lic); }
    public double Tax(SubDomains subSite, ProductLicenceType lic) { return PriceTax(VAT, subSite, lic); }
    public double Price(SubDomains subSite, ProductLicenceType lic) { return Price(VAT, subSite, lic); }

    public double PriceTax(int vat, SubDomains subSite, ProductLicenceType lic) { Currency cur = priceValue(subSite, lic); return priceTax(cur.Amount, cur.WithVat, vat); }
    public double Tax(int vat, SubDomains subSite, ProductLicenceType lic) { return (PriceTax(vat, subSite, lic) - Price(vat, subSite, lic)); }
    public double Price(int vat, SubDomains subSite, ProductLicenceType lic) { Currency cur = priceValue(subSite, lic); return price(cur.Amount, cur.WithVat, vat); }

    //public double PriceTax(SubDomains subSite, ProductLicenceType lic) { return PriceTax(VAT, subSite, lic); }
    //public double Tax(SubDomains subSite, ProductLicenceType lic) { return Tax(VAT, subSite, lic); }
    //public double Price(SubDomains subSite, ProductLicenceType lic) { return Price(VAT, subSite, lic); }

    public string PriceTaxStr(SubDomains subSite, ProductLicenceType lic) { return currencyString(PriceTax(subSite, lic)); }
    public string TaxStr(SubDomains subSite, ProductLicenceType lic) { return currencyString(Tax(subSite, lic)); }
    public string PriceStr(SubDomains subSite, ProductLicenceType lic) { return currencyString(Price(subSite, lic)); }

    //public override string ToString() { return PriceTax(subSite, lic).ToString("C", CurrencyCulture()); }

    private const int decimals = 1;
    public static double RoundCurrency(double value) {
      return Convert.ToDouble(Math.Round(Convert.ToDecimal(value), decimals));
    }
    static string currencyString(double amount) { return amount.ToString("F2", new CultureInfo("en-us")); }

    public string PayPalCurrencyTypeStr() { return Typ.ToString().ToUpper(); }


    public static string priceText(CurrencyType type, double price) {
      return price.ToString("C", CurrencyCulture(type));
    }
    public string PriceStr() { return priceText(Typ, Amount); }


    [XmlIgnore]
    public string AsString {
      get {
        byte[] am = BitConverter.GetBytes(Amount);
        byte[] res = new byte[am.Length + 2];
        res[0] = (byte)Typ;
        res[1] = (byte)(WithVat ? 1 : 0);
        am.CopyTo(res, 2);
        return ConvertNew.ToBase32(res, 12382, true);
      }
      set {
        byte[] res = ConvertNew.FromBase32(value, 12382);
        Typ = (CurrencyType)res[0];
        WithVat = res[1] != 0;
        Amount = BitConverter.ToDouble(res, 2);
      }
    }
    public static CultureInfo CurrencyCulture(CurrencyType type) {
      switch (type) {
        case CurrencyType.usd: return usaCulture;
        case CurrencyType.eur: return euroCulture;
        case CurrencyType.csk:
        case CurrencyType.czk: return kcCulture;
        case CurrencyType.pln: return plCulture;
        case CurrencyType.rub: return ruCulture;
        case CurrencyType.Try: return trCulture;
        case CurrencyType.vnd: return viCulture;
        case CurrencyType.ltl: return ltCulture;
        case CurrencyType.cny: return zhCulture;
        default: throw new NotImplementedException();
      }
    }

    public static CultureInfo kcCulture = new CultureInfo("cs-cz");
    static CultureInfo euroCulture = new CultureInfo("de-de");
    static CultureInfo usaCulture = new CultureInfo("en-us");
    static CultureInfo plCulture = new CultureInfo("pl-pl");
    static CultureInfo viCulture = new CultureInfo("vi-vn");
    static CultureInfo ltCulture = new CultureInfo("lt-lt");
    static CultureInfo ruCulture = new CultureInfo("ru-ru");
    static CultureInfo trCulture = new CultureInfo("tr-tr");
    static CultureInfo zhCulture = new CultureInfo("zh-cn");
  }



